first = int(input('정수를 입력'))
second = int(input('정수를 입력'))

sum_ = first + second
sub = first - second
mult = first * second
dlv = first / second
dlv_num = first // second
ram = first % second

print(first, "+", second, "=", sum_)
print(first, "-", second, "=", sub)
print(first, "*", second, "=", mult)
print(first, "/", second, "=", dlv)
print(first, "//", second, "=", dlv_num)
print(first, "%", second, "=", ram)


